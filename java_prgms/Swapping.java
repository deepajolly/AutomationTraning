class Swapping
{
static int a=20;
static int b=10;
public static void main(String args[])
{
System.out.println("before swapping a= "+a);
System.out.println("before swapping b= "+b);
int c=a;
int a=b;
int b=c;
System.out.println("after swapping a= "+a);
System.out.println("after swapping b= "+b);
}
}
