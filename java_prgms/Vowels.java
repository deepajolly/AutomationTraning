class Vowels
{
public static void main(String args[])
{
char letter='e';
switch(letter)
{
case 'a':System.out.print("a is a vowel");
break;
case 'e':System.out.print("e is a vowel");
break;
case 'i':System.out.print("i is a vowel");
break;
case 'o':System.out.print("o is a vowel");
break;
case 'u':System.out.print("u is a vowel");
break;
case 'A':System.out.print("A is a vowel");
break;
case 'E':System.out.print("E is a vowel");
break;
case 'I':System.out.print("I is a vowel");
break;
case 'O':System.out.print("O is a vowel");
break;
case 'U':System.out.print("U is a vowel");
break;
default:System.out.print("not a vowel");
}
}
}